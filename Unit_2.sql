-- let us talk about the upsert clause now which is suppose to do an action when there is a conflict with the primary key or unique constraint
--Syntax for upsert clause
insert into table_name (column_name1, column_name2, ...) values (value1, value2, ...)
on conflict (column_name) do {nothing | update set column_name1 = value1, column_name2 = value2, ... where condition};
--insert with Exclude constraint
insert into table_name (column_name1, column_name2, ...) values (value1, value2, ...)
on conflict on constraint constraint_name do update set  column_name = excluded.column_name where condition;

