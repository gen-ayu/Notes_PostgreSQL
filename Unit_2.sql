--Syntax for dropping table
drop table table_name;

--Different use cases and syntac for Alter table.
--Adding a column
alter table table_name add column column_name data_type constraint;
--Dropping a column
alter table table_name drop column column_name;
--Modifying a column data type
alter table table_name alter column column_name type new_data_type;
--Renaming a column
alter table table_name rename column old_column_name to new_column_name;
--Renaming a table
alter table old_table_name rename to new_table_name;
--Adding a constraint
alter table table_name add constraint constraint_name constraint_type (column_name);
--Dropping a constraint
alter table table_name drop constraint constraint_name;
--in case of adding multipe columns
alter table table_name add column column_name1 data_type constraint, add column column_name2 data_type constraint, ...;
--in case of dropping multiple columns
alter table table_name drop column column_name1, drop column column_name2, ...;
--in case of modifying multiple columns data type or adding multiple data type or even dropping multiple constraints
alter table table_name alter column column_name1 type new_data_type, alter column column_name2 type new_data_type, ...;
--in case of renaming multiple columns or tables
alter table old_table_name rename to new_table_name, rename column old_column_name1 to new_column_name1, rename column old_column_name2 to new_column_name2, ...;

