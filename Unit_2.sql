--We will now be talking about serial data type which is used for auto incrementing primary key
--Syntax for serial data type
create table table_name(
    id serial primary key,
    column_name data_type constraint,
    column_name data_type constraint default default_value,
    column_name data_type constraint,
    ...
);

--We will now be moving on to Identity data type which is used for auto incrementing primary key and it is more flexible than serial data type
--Syntax for identity data type
