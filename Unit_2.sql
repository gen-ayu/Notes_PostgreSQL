--like operator is used to search for a specific pattern in a column
--Syntax for like operator
select column_name from table_name where column_name like 'pattern';
-- the pattern can be defined using % and _ wildcards. % represents zero or more characters, while _ represents a single character.
--Example of like operator
select first_name from employees where first_name like 'J%'; -- this will return all the first names that start with J

--example of AND, OR, NOT, IN, BETWEEN, NOT IN
select * from employees where salary > 50000 and department_id = 1; -- this will return all the employees who have a salary greater than 50000 and belong to department 1
select * from employees where salary > 50000 or department_id = 1; -- this will return all the employees who have a salary greater than 50000 or belong to department 1
select * from employees where not (salary > 50000); -- this will return all the employees who do not have a salary greater than 50000
select * from employees where department_id in (1, 2, 3); -- this will return all the employees who belong to department 1, 2 or 3
select * from employees where salary between 50000 and 100000; -- this will return all the employees who have a salary between 50000 and 100000
select * from employees where department_id not in (1, 2, 3); -- this will return all the employees who do not belong to department 1, 2 or 3

--limit and offset clause
--Syntax for limit and offset clause
select column_name from table_name limit number_of_rows offset number_of_rows;
--Example of limit and offset clause
select * from employees limit 5; -- this will return the first 5 rows from the employees table
select * from employees offset 5; -- this will return all the rows from the employees table except the first 5 rows
select * from employees limit 5 offset 5; -- this will return 5 rows from the employees table starting from the 6th row

-- fetch clause is used to retrieve a specific number of rows from a result set
--Syntax for fetch clause  
select column_name from table_name fetch first number_of_rows rows only;
--Example of fetch clause
select * from employees fetch first 5 rows only; -- this will return the first 5

-- subqueries are used to retrieve data from one table based on the data from another table
--Syntax for subquery   
select column_name from table_name where column_name operator (select column_name from another_table where condition);
--Example of subquery  
select first_name from employees where department_id = (select department_id from departments where department_name = 'Sales'); 
-- this will return the first names of all the employees who belong to the Sales department

--subquery with exists operator( what this does is it checks if the subquery returns any rows or not if it returns then it will return true otherwise false)
select column_name from table_name where exists (select column_name from another_table where condition);

