-- let talk about cascade, set null, set default, Restrict, no action
--Cascade: When a record in the parent table is deleted or updated, the corresponding records in the child table are also deleted or updated.
--Set Null: When a record in the parent table is deleted or updated, the corresponding foreign key values in the child table are set to null.
--Set Default: When a record in the parent table is deleted or updated, the corresponding foreign key values in the child table are set to their default values.
--Restrict: When a record in the parent table is deleted or updated, the corresponding records in the child table are not deleted or updated, and an error is raised.
--No Action: When a record in the parent table is deleted or updated, the corresponding records in the child table are not deleted or updated, and no error is raised.

--syntax for set null
create table parent_table (
    id serial primary key,
    name varchar(50) not null
);
-- now for cascade
create table child_table (
    id serial primary key,
    parent_id int,
    name varchar(50) not null,
    foreign key (parent_id) references parent_table(id) on delete cascade on update cascade
);

--syntax for set null
create table child_table (
    id serial primary key,
    parent_id int,
    name varchar(50) not null,
    foreign key (parent_id) references parent_table(id) on delete set null on update set null
);
--syntax for set default
create table child_table (
    id serial primary key,
    parent_id int,
    name varchar(50) not null,
    foreign key (parent_id) references parent_table(id) on delete set default on update set default
);
--syntax for restrict
create table child_table (
    id serial primary key,
    parent_id int,
    name varchar(50) not null,
    foreign key (parent_id) references parent_table(id) on delete restrict on update restrict
);
--syntax for no action
create table child_table (
    id serial primary key,
    parent_id int,
    name varchar(50) not null,
    foreign key (parent_id) references parent_table(id) on delete no action on update no action
);

