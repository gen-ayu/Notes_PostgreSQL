-- grante and revoke statements are used to give and take away permissions from a user or role
--Syntax for grant statement
grant permission_type on object_name to role_name;
--Example of grant statement
grant select on employees to user_name; -- this will give select permission on employees table to user_name
grant all on employees to user_name; -- this will give all permissions on employees table to user_name
--Syntax for revoke statement
revoke permission_type on object_name from role_name;
--Example of revoke statement
revoke select on employees from user_name; -- this will take away select permission on employees table from user_name
revoke all on employees from user_name; -- this will take away all permissions on employees table from user_name


